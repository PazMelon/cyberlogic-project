export const applyGlobalTheme = (themeName: string) => {
  if (!themeName) return;

  // Save to local storage
  localStorage.setItem("cl-theme", themeName);

  // Normalize name for dataset attribute (e.g., light-classic instead of light_classic)
  const normalizedAttrTheme = themeName.replace("_", "-");
  document.documentElement.setAttribute("data-theme", normalizedAttrTheme);

  // Clear previous inline styling overrides first
  document.documentElement.removeAttribute("style");

  // If this theme is a preset CLI theme with specific variables, apply them inline
  const inlineProps = CLI_THEMES[themeName];
  if (inlineProps) {
    Object.entries(inlineProps).forEach(([variable, value]) => {
      document.documentElement.style.setProperty(variable, value);
    });
  }
};
